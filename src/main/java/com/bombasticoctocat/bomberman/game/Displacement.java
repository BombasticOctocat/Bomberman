package com.bombasticoctocat.bomberman.game;

public class Displacement {
    private final double x;
    private final double y;

    public Displacement(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
